	LIST	P=16F84A

PORTB	EQU	0X06
TRISB	EQU	0X86
STATUS	EQU	0X03
PCL	EQU	0X02
sayac	EQU	0X0C

	ORG	0
	CLRF	PORTB
	BSF	STATUS,5
	CLRF	TRISB
	BCF	STATUS,5

LOOP
	MOVLW	0X00
	CALL	CEVRIM_TAB
	MOVWF	PORTB
	CALL	GECIKME
	MOVLW	0X01
	CALL	CEVRIM_TAB
	MOVWF	PORTB
	CALL	GECIKME
	MOVLW	0X02
	CALL	CEVRIM_TAB
	MOVWF	PORTB
	CALL	GECIKME
	MOVLW	0X03
	CALL	CEVRIM_TAB
	MOVWF	PORTB
	CALL	GECIKME
	GOTO	LOOP

CEVRIM_TAB
	ADDWF	PCL,1 ; PCL+w
	RETLW	0X3F ; 0 -> 7 segment display
	RETLW	0X06 ; 1
	RETLW	0X5B ; 2
	RETLW	0X4F ; 3

GECIKME
	MOVLW	0XFF
	MOVWF	sayac

TEKRAR
	DECFSZ	sayac,1
	GOTO	TEKRAR
	RETURN
	END
